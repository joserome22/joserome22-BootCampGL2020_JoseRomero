package com.globallogic.encapsulamiento.ejercicio53;

public class Gato {
	
	private String nombre;
	private int patas;
	 
	public Gato() {}
	
	
	public String  getNombre() {
		return nombre;
	}
	public void setNombre(String nuevoNombre) {
		nombre=nuevoNombre;
		
	}
	public int  patas() {
		return patas;
	}
	public void setPatas(int numeroPatas) {
		patas=numeroPatas;
		
	}
	

}
